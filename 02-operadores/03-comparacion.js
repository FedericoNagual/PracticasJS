//Comparan las variables entre si , o variables con otras cosas
let a = 5;

console.log(a > 5);
console.log(a >= 5);
console.log(a < 5);
console.log(a <= 5);

//Siempre devuelve un booleano.
a = 10;

console.log(a == 10); //si es exactamente igual
console.log(a != 10); //Si son diferentes
console.log(a == '10'); //Pregunta por el valor y no por el tipo de dato TRUE
console.log(a === '10'); //Pregunta por el valor y por el tipo de dato FALSE
console.log(a !== '10');

